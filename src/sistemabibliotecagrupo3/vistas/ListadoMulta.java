    public void armarTabla(){
        ArrayList<Object> tabla = new ArrayList<Object>();
        tabla.add("Id Multa");
        tabla.add("Fecha Inicio");
        tabla.add("Fecha DevoluciÃ³n");
        tabla.add("Activo");
        
        for(Object ol : tabla){
            modelo.addColumn(ol);
        }
        
        tMultas.setModel(modelo);
    }
    
    public void borrarFilas(){
        int fi = modelo.getRowCount() - 1;
        
        for(int a = fi; a>=0; a--){
            modelo.removeRow(a);
        }
    }
    
    public void cargarDatos(){
        borrarFilas();
        String op = (String)cbEstado.getSelectedItem();
        
        switch(op){
            case "Todos": multas = (ArrayList)md.obtenerMultas(); break;
            case "Activos": multas = (ArrayList)md.obtenerMultasSegunEstado(true); break;
            case "Inactivos": multas = (ArrayList)md.obtenerMultasSegunEstado(false); break;
        }
        
        for(Multa m : multas){
            modelo.addRow(new Object[]{m.getId_Multa(), m.getFechaInicio(), m.getFechaFin(), m.isEstado()});
        }
    }

